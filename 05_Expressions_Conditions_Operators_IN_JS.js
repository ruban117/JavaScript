//Operators In JS

/*i) Arithmetic Operators*/

let a=10
let b=20
console.log("a + b =",a+b)
console.log("a - b =",a-b)
console.log("a / b =",a/b)
console.log("a * b =",a*b)
console.log("a ** b =",a**b)
console.log("a % b =",a%b)
console.log("a ++ =",a++)
console.log("a -- =",a--)
console.log("++a =",++a)
console.log("--a =",--a)

