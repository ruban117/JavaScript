/*Comparison Operator*/

let d=27,e="27"

console.log("d == e =",d==e)
console.log("d != e =",d!=e)
console.log("d === e =",d===e)
console.log("d !== e =",d!==e)
console.log("d > e =",d>e)
console.log("d < e =",d<e)
console.log("d >= e =",d>=e)
console.log("d <= e =",d<=e)

