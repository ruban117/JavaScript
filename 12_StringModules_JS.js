/*Here are the examples of some string modules*/

/*length*/

var n="Ruban"
console.log(n.length)

/*Uppercase*/

var n="Ruban"
console.log(n.toUpperCase())

/*Lowercase*/

var n="Ruban"
console.log(n.toLowerCase())

/*slice*/

var n="Ruban"
console.log(n.slice(2,4))

var n="Ruban"
console.log(n.slice(2))

/*Replace*/

var n="Ruban Bhai"
console.log(n.replace("Bhai","Bhau"))

/*Concatinate*/

var n="Ruban"
console.log(n.concat(" ","Soumita"))

var n="Ruban"
console.log(n+" "+"Soumita")

/*remove whitespace*/

var n="            Ruban         "
console.log(n.trim())

